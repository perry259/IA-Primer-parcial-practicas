# LAB#1 Working with the print() function

# Imprime una cadena de texto (string) con comillas dobles
print("marco") 

# Imprime una cadena de texto con comillas simples (totalmente válido)
print('marco') 

# Para imprimir 'marco' como variable, primero hay que definirla:
nombre = "marco"
print(nombre) 

# Si quieres imprimir dos cosas en la misma línea, usa una coma:
print("marco", "Python")
