# LAB#5 Variables

# Definimos los valores iniciales una sola vez
john = 3
mary = 5
adam = 6

# Operación de suma
totalApples = john + mary + adam
print("Total de manzanas (Suma):", totalApples)

# Operación de multiplicación (reutilizando las variables)
totalApples = john * mary * adam
print("Resultado de la multiplicación:", totalApples)

# Operación de resta
totalApples = john - mary - adam
print("Resultado de la resta:", totalApples)
