# LAB#9 Simple Input

# Solicitamos datos al usuario y los convertimos a punto flotante (decimal)
a = float(input("ingresa el primer numero: "))
b = float(input("ingresa el segundo numero: "))

# Mostramos los resultados de las 4 operaciones básicas
print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)

# El \n crea un espacio extra antes del mensaje final
print("\nThat's all, folks!")
